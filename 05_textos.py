nombre = input("Ingresa tu nombre: ")

print(nombre.upper()) 
#Este método upper() convierte todo nuestro texto a mayúsculas >>> JAVIER

print(nombre.capitalize()) 
#Este método upper() convierte la primera letra de nuestro texto a mayúscula >>> Javier

print(nombre.strip())
# Elimina los espacios vacíos

print(nombre.lower())
# Convierte el texto en minúsculas
print(nombre.replace('o', 'a'))
# Este método funciona como reemplazo de caracteres, en este caso si hay untexto que tenga la letra "O" será reemplazada por la letra "A"

print(nombre[0])
# Permite acceder a una posición dentro de nuesta cadena de caracteres. Estos parten desde 0 ... X

print(len(nombre))
# Perimte contabilizar cuántos caracteres tiene la cadena



